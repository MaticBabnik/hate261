// tcoeff that is read is
// sf_r rrrr llll (sign,first?,run,level)
export function decodeTcoeff(x: number): [number, number] {
    const neg = x & TCOEFF_SIGN_BIT;
    const level = x & 0xf;
    const run = (x >> 4) & 0x1f;

    return [run, neg ? -level : level]
}

